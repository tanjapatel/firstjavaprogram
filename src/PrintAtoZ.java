public class PrintAtoZ {

    public static void main(String[] args) {
        // print A To Z
        // initilaise character
        char c;
        for (c = 'A'; c<='Z';c++){
            System.out.print(" "+c);
        }
        System.out.println();
        for (c= 'a';c<='z';c++){
            System.out.print(" "+c);
        }

    }
}
